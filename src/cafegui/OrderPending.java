    private void btnSelesaiOrdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiOrdActionPerformed
        try {     
            StrSQL = "DELETE FROM order_info WHERE ID_Order = ?";
            PreparedStatement pStatement = Lconnection.prepareStatement(StrSQL);

            pStatement.setString(1, txtIDOrd.getText());
            

            if (pStatement.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Delete sukses", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Delete gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }

            pStatement.close();
        }
        catch (SQLException e) {
            System.out.println("koneksi gagal " + e.toString());
        }
        
        try{
            StrQry = "INSERT INTO history_info (Nama_Cust, Jenis_Meja, No_Meja, Pesanan, Total_Harga, Status)"
                   + "VALUES (?,?,?,?,?,?)";
        
            PreparedStatement pStatement = Lconnection.prepareStatement(StrQry);

            pStatement.setString(1, txtNamaOrd.getText());
            pStatement.setString(2, lblJenisOrd.getText());
            pStatement.setString(3, lblNoMejaOrd.getText());
            pStatement.setString(4, areaPesanOrd.getText());
            pStatement.setString(5, lblTotalOrd.getText());
            pStatement.setString(6, "Completed");

            if (pStatement.executeUpdate()>0){
                JOptionPane.showMessageDialog(this,
                    "Penambahan sukses", "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(this,
                    "Penambahan gagal", "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }

            pStatement.close();
            

        }
        catch (SQLException e){
            System.out.println("Koneksi gagal" + e.toString());
        }
    }//GEN-LAST:event_btnSelesaiOrdActionPerformed

