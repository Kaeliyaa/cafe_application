    private Object[][] getDataOrderInfo(){
        Object[][] dataOrderInfo = null;
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            url="jdbc:mysql://"+host+"/"+db+"?user="+user+"&password="+pswd;
            Connection conn = DriverManager.getConnection(url);
            
            Statement st = conn.createStatement();
            PreparedStatement pst = conn.prepareStatement("SELECT * FROM order_info", ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet rs = pst.executeQuery();
            
            rs.last();
            int rowCount = rs.getRow();
            rs.beforeFirst();
            
            dataOrderInfo = new Object[rowCount][7];
            int no = -1;
            
            while(rs.next()){
                no = no + 1;
                dataOrderInfo[no][0] = rs.getString("ID_Order");
                dataOrderInfo[no][1] = rs.getString("Nama_Cust");
                dataOrderInfo[no][2] = rs.getString("Jenis_Meja");
                dataOrderInfo[no][3] = rs.getString("No_Meja");
                dataOrderInfo[no][4] = rs.getString("Pesanan");
                dataOrderInfo[no][5] = rs.getString("Total_Harga");
                dataOrderInfo[no][6] = rs.getString("Status");
            }
            st.close();
            conn.close();
            
        }
        catch (ClassNotFoundException e){
            System.out.println("jdbc.Driver tidak ditemukan");
        }
        catch (SQLException e){
            System.out.println("Koneksi gagal" + e.toString());
        }
        return dataOrderInfo;
    }
    
